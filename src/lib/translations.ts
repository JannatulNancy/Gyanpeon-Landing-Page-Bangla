/**
 * Highly robust translation helper that checks for exact key matches,
 * then falls back to case-insensitive and trimmed lookups, and lastly
 * falls back to returning the original string.
 */
export function translate(text: string | null | undefined, lang: 'en' | 'bn'): string {
  if (!text) return '';
  if (lang === 'en') return text;

  const trimmedText = text.trim();
  
  // Exact match
  if (TRANSLATIONS[trimmedText]) {
    return TRANSLATIONS[trimmedText];
  }

  // Substring or case-insensitive fallback mapping
  const lowerText = trimmedText.toLowerCase();
  for (const [key, val] of Object.entries(TRANSLATIONS)) {
    if (key.toLowerCase() === lowerText) {
      return val;
    }
  }

  // Specific common sub-word translations to handle custom admin strings
  let processed = trimmedText;

  // Numbers translation (for prices or dates)
  if (lang === 'bn') {
    processed = processed
      .replace(/\bFree Plan\b/g, 'ফ্রি প্ল্যান')
      .replace(/\bActive Now\b/g, 'সক্রিয় রয়েছে')
      .replace(/\bPurchase\b/g, 'ক্রয় করুন')
      .replace(/\bRunning\b/g, 'চলমান')
      .replace(/\b1yr\b/gi, '১ বছর')
      .replace(/\b1 year\b/gi, '১ বছর')
      .replace(/(\d+)/g, (num) => {
        const bnNums = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
        return num.split('').map(char => bnNums[Number(char)] || char).join('');
      });
  }

  return processed;
}
